class TestMessageConsistency:
    """
    Tests for consistency between Farsi and English messages.
    """

    @given(data=solver_data_with_insufficient_class())
    @settings(max_examples=100, deadline=5000)
    def test_farsi_and_english_messages_convey_same_info(
        self, data: Dict[str, Any]
    ):
        """
        **Feature: solver-afghanistan-features, Property 9: Farsi Message Localization**
        **Validates: Requirements 6.5**
        
        WHEN messages are generated THEN the Farsi and English versions SHALL
        convey the same information (same period counts).
        """
        validator = MinistryValidator(enabled=True, mode=ValidationMode.WARN)
        result = validator.validate(data)
        
        all_messages = result.warnings + result.errors
        assume(len(all_messages) > 0)
        
        for message in all_messages:
            farsi_msg = message['messageFarsi']
            english_msg = message['messageEnglish']
            msg_type = message.get('type', '')
            
            if msg_type == 'MINISTRY_SUBJECT_HOURS':
                required = str(message.get('requiredPeriods', 0))
                configured = str(message.get('configuredPeriods', 0))
                # Both messages should contain the same period counts
                assert required in farsi_msg, "Farsi message should contain required periods"
                assert required in english_msg, "English message should contain required periods"
                assert configured in farsi_msg, "Farsi message should contain configured periods"
                assert configured in english_msg, "English message should contain configured periods"
            elif msg_type == 'TOTAL_PERIODS_MISMATCH':
                expected = str(message.get('expectedPeriods', 0))
                actual = str(message.get('actualPeriods', 0))
                # Both messages should contain the same period counts
                assert expected in farsi_msg, "Farsi message should contain expected periods"
                assert expected in english_msg, "English message should contain expected periods"
                assert actual in farsi_msg, "Farsi message should contain actual periods"
                assert actual in english_msg, "English message should contain actual periods"

    @given(data=solver_data_with_insufficient_class(), mode=validation_mode_enabled())
    @settings(max_examples=100, deadline=5000)
    def test_message_severity_matches_mode(
        self, data: Dict[str, Any], mode: ValidationMode
    ):
        """
        **Feature: solver-afghanistan-features, Property 9: Farsi Message Localization**
        **Validates: Requirements 6.5**
        
        WHEN messages are generated THEN the severity SHALL match the validation mode.
        In strict mode:
          - Core subjects produce errors
          - TOTAL_PERIODS_MISMATCH produces errors
          - Non-core subjects produce warnings
        In warn mode, all issues produce warnings.
        """
        validator = MinistryValidator(enabled=True, mode=mode)
        result = validator.validate(data)
        
        all_messages = result.warnings + result.errors
        assume(len(all_messages) > 0)
        
        for message in all_messages:
            msg_type = message.get('type', '')
            is_core = message.get('isCore', False)
            
            if mode == ValidationMode.STRICT:
                # In strict mode: core subjects and total mismatch are errors
                if is_core or msg_type == 'TOTAL_PERIODS_MISMATCH':
                    assert message['severity'] == 'error', f"Strict mode should produce errors for core subjects and total mismatch"
                else:
                    assert message['severity'] == 'warning', "Non-core subjects should produce warnings"
            else:
                # In warn mode, all issues are warnings
                assert message['severity'] == 'warning', "Warn mode should produce warnings"
