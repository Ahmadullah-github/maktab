@st.composite
def valid_class_name(draw) -> str:
    """Generate a valid class name in Farsi or English."""
    # Mix of Farsi and English class names
    names = [
        "صنف ۱-الف",
        "صنف ۲-ب",
        "صنف ۱۰-الف",
        "Class 1A",
        "Grade 5B",
        "صنف ۷",
    ]
    return draw(st.sampled_from(names))


@st.composite
def valid_grade_level(draw) -> int:
    """Generate a valid grade level (1-12)."""
    return draw(st.sampled_from(list(MINISTRY_CURRICULUM.keys())))


@st.composite
def subject_with_insufficient_periods(draw, grade: int) -> Dict[str, Any]:
    """Generate a subject requirement with insufficient periods for the grade."""
    subjects = MINISTRY_CURRICULUM[grade]
    subject = draw(st.sampled_from(subjects))
    subject_name = subject['name']
    min_periods = subject['periodsPerWeek']
    
    # Generate periods less than minimum
    configured_periods = draw(st.integers(min_value=0, max_value=max(0, min_periods - 1)))
    
    return {
        'subject_name': subject_name,
        'configured_periods': configured_periods,
        'min_periods': min_periods,
    }


@st.composite
def class_with_insufficient_subjects(draw) -> Dict[str, Any]:
    """Generate a class with at least one subject having insufficient periods."""
    grade = draw(valid_grade_level())
    class_name = draw(valid_class_name())
    class_id = f"class-{draw(st.integers(min_value=1, max_value=100))}"
    
    # Get curriculum subjects for this grade
    curriculum_subjects = MINISTRY_CURRICULUM[grade]
    category = get_grade_category(grade)
    
    # Create subject requirements with at least one insufficient
    subject_reqs = {}
    subjects = []
    
    for i, subj_info in enumerate(curriculum_subjects):
        subj_id = f"subj-{i}"
        subjects.append({
            'id': subj_id,
            'name': subj_info['name'],
        })
        
        min_periods = subj_info['periodsPerWeek']
        
        # Make at least one subject insufficient
        if i == 0:
            # First subject is always insufficient
            periods = draw(st.integers(min_value=0, max_value=max(0, min_periods - 1)))
        else:
            # Others may or may not be sufficient
            periods = draw(st.integers(min_value=0, max_value=min_periods + 2))
        
        subject_reqs[subj_id] = {'periodsPerWeek': periods}
    
    return {
        'class': {
            'id': class_id,
            'name': class_name,
            'gradeLevel': grade,
            'category': category.value if category else None,
            'subjectRequirements': subject_reqs,
        },
        'subjects': subjects,
    }


@st.composite
def solver_data_with_insufficient_class(draw) -> Dict[str, Any]:
    """Generate solver input data with at least one class having insufficient subjects."""
    class_data = draw(class_with_insufficient_subjects())
    
    return {
        'config': {
            'enableMinistryValidation': True,
            'ministryValidationMode': 'warn',
        },
        'classes': [class_data['class']],
        'subjects': class_data['subjects'],
        'teachers': [],
        'rooms': [],
    }


@st.composite
def validation_mode_enabled(draw) -> ValidationMode:
    """Generate an enabled validation mode (WARN or STRICT)."""
    return draw(st.sampled_from([ValidationMode.WARN, ValidationMode.STRICT]))


